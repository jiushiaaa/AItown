// æ ¹æ®è¯„åˆ†è·å–æ˜Ÿæ˜Ÿ
function getStars(score) {
  const fullStars = Math.floor(score);
  const halfStar = score - fullStars >= 0.5;
  let stars = '';
  
  // æ·»åŠ å®å¿ƒæ˜Ÿæ˜Ÿ
  for (let i = 0; i < fullStars; i++) {
    stars += 'â˜…';
  }
  
  // æ·»åŠ åŠæ˜Ÿ
  if (halfStar) {
    stars += 'â˜†';
  }
  
  return stars;
}

